/**
 * index.ts — SidecarApi: the unified API client for the Bfloat sidecar.
 *
 * The SidecarApi class wires together all domain-specific sub-clients
 * (terminal, filesystem, agent) behind a single authenticated HTTP client.
 *
 * It is initialised once — after the Tauri `await_initialization` command
 * resolves — and then accessed via the module-level `getSidecarApi()` /
 * `getSidecarApiSync()` helpers (mirroring the `getPlatform()` pattern used
 * elsewhere in the desktop package).
 *
 * Usage:
 *
 *   // In your app boot sequence (e.g. ServerGate in entry.tsx):
 *   import { initialiseSidecarApi, getSidecarApi } from "./api"
 *
 *   const { url, password } = await invoke("await_initialization", ...)
 *   initialiseSidecarApi(url, password)
 *
 *   // Anywhere else in the renderer:
 *   const api = getSidecarApiSync()
 *   const result = await api.terminal.create("my-terminal", "/home/user/project")
 *
 * Migration note:
 *   The sub-clients expose the same logical methods as the Electron conveyor
 *   APIs so that existing renderer code needs only minimal changes:
 *
 *     // Before (Electron):
 *     window.conveyor.terminal.create(id, cwd)
 *     window.conveyor.filesystem.readFile(path)
 *     window.conveyor.aiAgent.createSession(opts)
 *
 *     // After (Tauri):
 *     const api = getSidecarApiSync()
 *     api.terminal.create(id, cwd)
 *     api.filesystem.readFile(path)
 *     api.agent.createSession(opts)
 */

