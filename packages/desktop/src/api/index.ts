/**
 * The unified API client for the Bfloat sidecar.
 *
 * Instantiate once with the URL and password returned by `await_initialization`,
 * then use the sub-client properties to make requests.
 */
export class SidecarApi {
  /** The underlying HTTP client.  Exposed for advanced usage (e.g. custom routes). */
  readonly http: HttpClient

  /** Terminal session management and PTY I/O streaming. */
  readonly terminal: TerminalApi

  /** Filesystem operations (read, write, stat, mkdir, …). */
  readonly filesystem: FilesystemApi

  /** AI agent session management and streaming responses. */
  readonly agent: AgentApi

  /**
   * @param url       Full base URL of the sidecar (e.g. "http://127.0.0.1:4000").
   * @param password  The UUID password generated by the Tauri backend.
   *                  Pass null when connecting to an external server without auth.
   */
  constructor(url: string, password: string | null | undefined) {
    this.http = new HttpClient(url, password)
    this.terminal = new TerminalApi(this.http)
    this.filesystem = new FilesystemApi(this.http)
    this.agent = new AgentApi(this.http)
  }
}

// ---------------------------------------------------------------------------
// Module-level singleton
// ---------------------------------------------------------------------------

let _api: SidecarApi | null = null

/**
 * Initialise the module-level SidecarApi singleton.
 *
 * This must be called exactly once, after the Tauri `await_initialization`
 * command resolves and before any API calls are made.
 *
 * Calling this a second time replaces the existing instance (useful in tests).
 *
 * @param url       Sidecar base URL from `ServerReadyData.url`.
 * @param password  Sidecar password from `ServerReadyData.password`.
 */
export function initialiseSidecarApi(
  url: string,
  password: string | null | undefined,
): SidecarApi {
  _api = new SidecarApi(url, password)
  return _api
}

/**
 * Return the module-level SidecarApi singleton.
 *
 * Throws `Error` if `initialiseSidecarApi()` has not been called yet.
 * Use this in hot paths where async overhead is undesirable.
 *
 * @throws {Error} When called before `initialiseSidecarApi()`.
 */
export function getSidecarApiSync(): SidecarApi {
  if (!_api) {
    throw new Error(
      "SidecarApi has not been initialised yet. Call initialiseSidecarApi() first.",
    )
  }
  return _api
}

/**
 * Return the module-level SidecarApi singleton asynchronously.
 *
 * Polls until the singleton is available, making it safe to call immediately
 * after mounting the React tree (before `initialiseSidecarApi()` completes).
 *
 * The poll interval is 20 ms with a 10-second timeout.
 *
 * @throws {Error} When the singleton is not initialised within 10 seconds.
 */
export async function getSidecarApi(): Promise<SidecarApi> {
  if (_api) return _api

  const POLL_INTERVAL = 20
  const TIMEOUT = 10_000
  const start = Date.now()

  return new Promise<SidecarApi>((resolve, reject) => {
    const interval = setInterval(() => {
      if (_api) {
        clearInterval(interval)
        resolve(_api)
        return
      }

      if (Date.now() - start > TIMEOUT) {
        clearInterval(interval)
        reject(
          new Error(
            "Timed out waiting for SidecarApi to be initialised. " +
              "Ensure initialiseSidecarApi() is called during app boot.",
          ),
        )
      }
    }, POLL_INTERVAL)
  })
}
